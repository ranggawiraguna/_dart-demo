void main(List<String> args) {
  print("\n");

  //=============================================================================================================================
  // Increment Value
  int a = 0;
  print("Nilai a Awal      = " + a.toString());

  a = a + 2;
  print("Nilai a Increment = " + a.toString());

  print("\n");
  //=============================================================================================================================

  //=============================================================================================================================
  // Decrement Value
  int b = 4;
  print("Nilai b Awal      = " + a.toString());

  b = b - 2;
  print("Nilai b Decrement = " + a.toString());

  print("\n");
  //=============================================================================================================================

  //=============================================================================================================================
  // For Increment
  for (int i = 2; i <= 10; i = i + 2) {
    print("Nilai pencacah = " + i.toString());
  }

  print("\n");
  //=============================================================================================================================

  //=============================================================================================================================
  // For Decrement
  for (int i = 10; i >= 2; i = i - 2) {
    print("Nilai pencacah = " + i.toString());
  }

  print("\n");
  //=============================================================================================================================

  //=============================================================================================================================
  // Unary Increment
  int c = 1;
  print("Nilai c Awal        = " + c.toString());

  c++;
  print("Nilai c Increment-1 = " + c.toString());

  print("Nilai c Increment-2 = " + (c++).toString());

  print("Nilai c Increment-3 = " + (++c).toString());

  print("\n");
  //=============================================================================================================================

  //=============================================================================================================================
  // Unary Decrement
  int d = 11;
  print("Nilai d Awal        = " + d.toString());

  d--;
  print("Nilai d Decrement-1 = " + d.toString());

  print("Nilai d Decrement-2 = " + (d--).toString());

  print("Nilai d Decrement-3 = " + (--d).toString());

  print("\n");
  //=============================================================================================================================

  //=============================================================================================================================
  // For Unary Increment
  for (int i = 1; i <= 10; i++) {
    print("Nilai pencacah = " + i.toString());
  }

  print("\n");
  //=============================================================================================================================

  //=============================================================================================================================
  // For Unary Decrement
  for (int i = 10; i >= 1; i--) {
    print("Nilai pencacah = " + i.toString());
  }

  print("\n");
  //=============================================================================================================================

  //=============================================================================================================================
  // While
  int e = 1;
  while (e <= 10) {
    print("Nilai pencacah = " + e.toString());
    e++;
  }

  print("\n");
  //=============================================================================================================================

  //=============================================================================================================================
  // Do While
  int f = 10;
  do {
    print("Nilai pencacah = " + f.toString());
    f--;
  } while (f >= 1);

  print("\n");
  //=============================================================================================================================
}
